// WHILE LOOP
let index=0;
while(index<=10)
{
    console.log(`Value of index is ${index}`);
    index+=2;
}

let myArr=["Batman", "IronMan", "Goole"];
let arr=0;
while(arr<myArr.length)
{
    console.log(`Value is ${myArr[arr]}`);
    arr++;
}

// DO WHILE LOOP
let score=11;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score<=10);